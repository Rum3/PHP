<?php
/*Създайте регулярен израз, който съответства на телефонни 
номера в различни формати, като (123) 456-7890 или 
123-456-7890*/
$pattern = "/^\(?\d{3}\)?[-.\s]?\d{3}[-.\s]?\d{4}$/";
$phone_number = "(123) 456-7890";
if (preg_match($pattern, $phone_number)) {
  echo "Валиден телефонен номер";
} else {
  echo "Невалиден телефонен номер";
}
